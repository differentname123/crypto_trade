# ================= 新增逻辑 4：最终的分数计算与附加字段 =========================
# ============================================================================
def calculate_final_score(param_list: list, margin_info: dict, output_file: str = "grid_statistics_result.csv"):
    """
    新增的最终计算分数的函数。
    逻辑：
    1. 先 generate_statistics 得到每个币的统计信息
    2. 拉取每个币的最新价格
    3. 根据最新价格和理论最低价计算当前价格距离最低价的回撤百分比
    4. 得到这个百分比后查询距离最近的 margin_info，得到相应的所需资金
    5. 最终分数 = (Avg_Score / 所需资金) * 10000
    """
    # 1. 得到基础统计信息
    df = generate_statistics(param_list, output_file)
    if df is None or df.empty:
        return None

    # 2. 拉取每个币的最新价格
    latest_prices = {}
    for param in param_list:
        csv_file_path = param["csv_file_path"]
        try:
            coin_name = os.path.basename(csv_file_path).split('_')[0]
            temp_df = pd.read_csv(csv_file_path)
            if not temp_df.empty:
                # 处理时间戳，确保按时间排序后再取最后一条数据作为最新价格
                if 'open_time' in temp_df.columns:
                    if not pd.api.types.is_datetime64_any_dtype(temp_df['open_time']):
                        temp_df['open_time'] = pd.to_numeric(temp_df['open_time'])
                    temp_df = temp_df.sort_values('open_time')
                # 提取最新价格（最后一行 close 价格）
                latest_prices[coin_name] = float(temp_df['close'].iloc[-1])
        except Exception as e:
            print(f"[{csv_file_path}] 拉取最新价格失败: {e}")

    df['Latest_Price'] = df['Coin'].map(latest_prices)

    # 3. 计算当前价格距离理论最低价的回撤百分比
    # 逻辑: (最新价格 - 理论最低价) / 最新价格 * 100
    def calc_dd(row):
        lp = row.get('Latest_Price')
        tl = row.get('Theory_Lowest_Price')
        if pd.notna(lp) and pd.notna(tl) and lp > 0:
            return (lp - tl) / lp * 100
        return None

    df['Current_to_Lowest_DD(%)'] = df.apply(calc_dd, axis=1)

    # 4. 查询距离最近的 margin_info，得到相应的所需资金
    def get_required_funds(dd_pct):
        if pd.isna(dd_pct):
            return None
        # 寻找 keys 中与 dd_pct 差值绝对值最小的 key
        closest_key = min(margin_info.keys(), key=lambda k: abs(k - dd_pct))
        return margin_info[closest_key]

    df['Required_Funds'] = df['Current_to_Lowest_DD(%)'].apply(get_required_funds)

    # 5. 最终分数 = (avg_score / 所需资金) * 10000
    def calc_final_score(row):
        avg = row.get('Avg_Score')
        req = row.get('Required_Funds')
        if pd.notna(avg) and pd.notna(req) and req > 0:
            return (avg / req) * 10000
        return None

    df['Final_Score'] = df.apply(calc_final_score, axis=1)

    # 为了美观，将新增字段放到所有列最后
    ordered_columns = list(df.columns)
    new_cols = ['Latest_Price', 'Current_to_Lowest_DD(%)', 'Required_Funds', 'Final_Score']
    for col in new_cols:
        if col in ordered_columns:
            ordered_columns.remove(col)
    ordered_columns.extend(new_cols)
    df = df[ordered_columns]

    # 根据最终分数进行降序排列
    df.sort_values(by='Final_Score', ascending=False, inplace=True)
    df.reset_index(drop=True, inplace=True)

    # 覆盖原文件保存持久化
    try:
        df.to_csv(output_file, index=False)
    except Exception as e:
        print(f"持久化保存带最终分数的统计文件失败: {e}")

    return df


